package co.gov.ideamredd.entities;

public class EstadoBiomasa {
	
	private Integer consecutivo;
	private String verificado;
	private String atipico;
	private String incluidoCalculos;

	public Integer getConsecutivo() {
		return consecutivo;
	}

	public void setConsecutivo(Integer consecutivo) {
		this.consecutivo = consecutivo;
	}

	public String getVerificado() {
		return verificado;
	}

	public void setVerificado(String verificado) {
		this.verificado = verificado;
	}

	public String getAtipico() {
		return atipico;
	}

	public void setAtipico(String atipico) {
		this.atipico = atipico;
	}

	public String getIncluidoCalculos() {
		return incluidoCalculos;
	}

	public void setIncluidoCalculos(String incluidoCalculos) {
		this.incluidoCalculos = incluidoCalculos;
	}

}
